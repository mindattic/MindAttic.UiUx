# ====================================================================
# MindAttic.Content -> StreetSamurai
# 1. Overwrites wwwroot/js/{loader,tv-static,home-bg,console-bg}.js from Cyberspace/
# 2. Overwrites wwwroot/js/pin-footer.js from PinFooter/ (separate group)
# 3. Rewrites the marker block in wwwroot/app.css with Cyberspace/frontpage.css contents
#
# StreetSamurai's App.razor already <script>-loads the JS files. The first run
# of this script also splices a marker block into app.css; subsequent runs
# replace the marker contents in-place.
#
# Usage:
#   powershell -File sync-streetsamurai.ps1
#   powershell -File sync-streetsamurai.ps1 -BlazorRoot 'D:/path/StreetSamurai.Blazor'
# ====================================================================
[CmdletBinding()]
param(
    [string]$ContentRoot,
    [string]$BlazorRoot = 'D:/Projects/MindAttic/StreetSamurai/v3/StreetSamurai.Blazor'
)

$ErrorActionPreference = 'Stop'

if (-not $ContentRoot) {
    $here = if ($PSScriptRoot) { $PSScriptRoot } else { Split-Path -Parent $MyInvocation.MyCommand.Path }
    $ContentRoot = Split-Path -Parent $here
}

$utf8 = [System.Text.UTF8Encoding]::new($false)

$cyberspace    = Join-Path $ContentRoot 'Cyberspace'
$wwwJs  = Join-Path $BlazorRoot 'wwwroot/js'
$appCss = Join-Path $BlazorRoot 'wwwroot/app.css'

foreach ($p in @($cyberspace, $wwwJs, $appCss)) {
    if (-not (Test-Path $p)) { throw "Path not found: $p" }
}

# JS files: straight byte-level overwrite (Copy-Item preserves bytes)
$jsFiles = @('loader.js', 'tv-static.js', 'home-bg.js', 'console-bg.js')
foreach ($f in $jsFiles) {
    Copy-Item -Path (Join-Path $cyberspace $f) -Destination (Join-Path $wwwJs $f) -Force
    $kb = [math]::Round(((Get-Item (Join-Path $wwwJs $f)).Length / 1024), 1)
    Write-Output "  [JS]  $f  ($kb KB)"
}

# pin-footer.js lives in its own group dir (PinFooter/), separate from CYBERSPACE.
$pinFooter = Join-Path $ContentRoot 'PinFooter'
if (-not (Test-Path $pinFooter)) { throw "Path not found: $pinFooter" }
Copy-Item -Path (Join-Path $pinFooter 'pin-footer.js') -Destination (Join-Path $wwwJs 'pin-footer.js') -Force
$kb = [math]::Round(((Get-Item (Join-Path $wwwJs 'pin-footer.js')).Length / 1024), 1)
Write-Output "  [JS]  pin-footer.js  ($kb KB)"

# CSS: rewrite marker block in app.css. Use UTF-8 reads to preserve non-ASCII.
$css     = [System.IO.File]::ReadAllText((Join-Path $cyberspace 'frontpage.css'), $utf8)
$cssText = [System.IO.File]::ReadAllText($appCss, $utf8)

$begin = '/* == BEGIN MINDATTIC.CONTENT:CYBERSPACE.CSS == */'
$end   = '/* == END MINDATTIC.CONTENT:CYBERSPACE.CSS == */'

if (-not $cssText.Contains($begin)) {
    throw "Marker '$begin' not found in $appCss. Insert the marker pair manually first (delete the CYBERSPACE section it replaces), then re-run sync."
}

$block = $begin + "`r`n" +
         "/* Generated by MindAttic.Content/sync/sync-streetsamurai.ps1. Do not edit by hand." + "`r`n" +
         "   Edit MindAttic.Content/Cyberspace/frontpage.css and re-run the sync. */" + "`r`n" +
         $css + "`r`n" +
         $end

$startIdx = $cssText.IndexOf($begin)
$endIdx   = $cssText.IndexOf($end, $startIdx)
if ($endIdx -lt 0) { throw "End marker '$end' not found after begin marker in $appCss." }
$endIdx  += $end.Length

$updated = $cssText.Substring(0, $startIdx) + $block + $cssText.Substring($endIdx)
[System.IO.File]::WriteAllText($appCss, $updated, $utf8)

$kb = [math]::Round(((Get-Item $appCss).Length / 1024), 1)
Write-Output "  [CSS] app.css marker block updated ($kb KB)"
Write-Output "Synced MindAttic.Content -> $BlazorRoot"
